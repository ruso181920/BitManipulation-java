public class BitManipulteBasic {
    public static void main(String[] args) {
        //binary and
        System.out.println(5&3);
         //binary or
        System.out.println(5|3);
         //binary Xor
        System.out.println(5^3);

       //binary not
        System.out.println(~-5);
         //binary left shift
        System.out.println(51<<3);
         //binary right shift
        System.out.println(50>>3);

    }
}
